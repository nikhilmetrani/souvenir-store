			Double totalPriceOfItems = (double) (20.0f*5)+(double)(10.0f*20);
			Double totalPriceBeforeDiscount = shoppingCart3.getTotalPriceBeforeDiscount(shoppingCart2.getItems());
			assertNotSame(totalPriceBeforeDiscount,0.0);
